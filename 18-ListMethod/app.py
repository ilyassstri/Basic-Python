numbers = [5, 6, 7, 8, 1]
print(numbers)

numbers.append(99) # Menambahkan nomber di urutan paling belakang.
print(numbers)

numbers.insert(2, 100) # Menambahkan nomber pada urukan ke dua, dengan nomber 100
print(numbers)

numbers.pop(5)
print(numbers)

numbers.remove(8) # Menghapus angka 8
print(numbers)

numbers.sort() # Mengurutkan dari angka yang paling kecil ke yang paling besar.
print(numbers)